// Get available languages
export const getAvailableLanguages = () => {
  return Object.keys(languageData).map(code => ({
    code,
    name: languageData[code].name
  }));
};

// Get translation for a key
export const getTranslation = (languageCode, key) => {
  if (!languageData[languageCode] || !languageData[languageCode].translations[key]) {
    return languageData.en.translations[key] || key;
  }
  return languageData[languageCode].translations[key];
};

// Get all translations for a language
export const getTranslations = (languageCode) => {
  if (!languageData[languageCode]) {
    return languageData.en.translations;
  }
  return languageData[languageCode].translations;
};