// Generate random blood stock data
export const generateBloodStock = () => {
  const bloodTypes = ['A+', 'A-', 'B+', 'B-', 'AB+', 'AB-', 'O+', 'O-'];
  const stock = {};
  
  bloodTypes.forEach(type => {
    stock[type] = {
      available: Math.floor(Math.random() * 50) + 1, // 1-50 units
      lastUpdated: new Date(Date.now() - Math.random() * 7 * 24 * 60 * 60 * 1000).toLocaleDateString(), // Within last 7 days
      status: Math.random() > 0.3 ? 'Available' : 'Critical' // 70% available, 30% critical
    };
  });
  
  return stock;
};

// Get all states
export const getStates = () => {
  return Object.keys(hospitalData).sort();
};

// Get districts for a state
export const getDistricts = (state) => {
  if (!state || !hospitalData[state]) return [];
  return Object.keys(hospitalData[state]).sort();
};

// Get cities for a district
export const getCities = (state, district) => {
  if (!state || !district || !hospitalData[state] || !hospitalData[state][district]) return [];
  return Object.keys(hospitalData[state][district]).sort();
};

// Get hospitals for a city
export const getHospitals = (state, district, city) => {
  if (!state || !district || !city || 
      !hospitalData[state] || 
      !hospitalData[state][district] || 
      !hospitalData[state][district][city]) return [];
  
  return hospitalData[state][district][city].map(hospital => ({
    ...hospital,
    bloodStock: generateBloodStock()
  }));
};

// Search hospitals with blood banks
export const searchBloodBanks = (state, district, city, bloodGroup) => {
  const hospitals = getHospitals(state, district, city);
  let results = hospitals.filter(hospital => hospital.bloodBank);
  
  if (bloodGroup) {
    results = results.filter(hospital => 
      hospital.bloodStock[bloodGroup] && 
      hospital.bloodStock[bloodGroup].available > 0
    );
  }
  
  return results;
};